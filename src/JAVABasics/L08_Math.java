package JAVABasics;

public class L08_Math {

    public static void main(String[] args) {

        RandoNum();
        MaxNum();
        MinNUm();
    }

    public static void RandoNum() {
        System.out.println(Math.random() * 100);

    }

    public static void MaxNum() {
        System.out.println(Math.max(10, 2) * 100);

    }

    public static void MinNUm() {
        System.out.println(Math.min(10, 2) * 100);

    }

}
